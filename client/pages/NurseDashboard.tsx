// ─── Helpers ──────────────────────────────────────────────────────────────────
function uid() { return Math.random().toString(36).slice(2, 9); }
function formatTime(ts: number) {
  return new Date(ts).toLocaleTimeString([], { hour: "2-digit", minute: "2-digit" });
}
function formatElapsed(ts: number): string {
  const ms = Date.now() - ts;
  const mins = Math.floor(ms / 60000);
  if (mins < 60) return `${mins}m ago`;
  return `${Math.floor(mins / 60)}h ${mins % 60}m ago`;
}
function formatDate(ts: number): string {
  return new Date(ts).toLocaleDateString("en-IN", { day: "numeric", month: "short", hour: "2-digit", minute: "2-digit" });
}
function isVitalAbnormal(v: Vitals): boolean {
  return v.oxygen < 92 || v.heartRate > 110 || v.heartRate < 50 || v.temperature > 38.5 ||
    v.bpSystolic > 160 || v.respiratoryRate > 22;
}

function computeRiskScore(v: Vitals, severity: Severity): number {
  let score = 0;
  if (severity === "critical") score += 40;
  else if (severity === "moderate") score += 20;
  if (v.oxygen < 88) score += 25;
  else if (v.oxygen < 92) score += 15;
  if (v.heartRate > 130 || v.heartRate < 45) score += 20;
  else if (v.heartRate > 110 || v.heartRate < 55) score += 10;
  if (v.bpSystolic > 180) score += 15;
  if (v.temperature > 39.5) score += 15;
  if (v.respiratoryRate > 25) score += 10;
  return Math.min(100, score);
}

function computeRiskFactors(v: Vitals, severity: Severity, disease: string): string[] {
  const factors: string[] = [];
  if (v.oxygen < 92) factors.push("Hypoxemia");
  if (v.heartRate > 110) factors.push("Tachycardia");
  if (v.heartRate < 50) factors.push("Bradycardia");
  if (v.bpSystolic > 160) factors.push("Hypertensive crisis");
  if (v.temperature > 38.5) factors.push("Fever");
  if (v.respiratoryRate > 22) factors.push("Tachypnea");
  if (severity === "critical") factors.push("Critical diagnosis");
  if (disease.toLowerCase().includes("septic")) factors.push("Sepsis risk");
  if (disease.toLowerCase().includes("cardiac") || disease.toLowerCase().includes("stemi")) factors.push("Cardiac event");
  return factors;
}

function computeEscalationLevel(riskScore: number, severity: Severity): EscalationLevel {
  if (riskScore >= 80 || severity === "critical") return 5;
  if (riskScore >= 60) return 4;
  if (riskScore >= 40) return 3;
  if (riskScore >= 20) return 2;
  return 1;
}

function getTaskIntelligence(label: string, priority: Priority): TaskIntelligence {
  if (label.includes("⚠") || label.includes("immediate") || priority === "P1") return "emergency";
  if (label.includes("critical") || label.includes("doctor") || priority === "P2") return "urgent";
  if (label.includes("monitor") || label.includes("update")) return "advisory";
  return "routine";
}

function generateEcgPoints(heartRate: number, abnormal: boolean): number[] {
  const points: number[] = [];
  const cycles = 6;
  const perCycle = 20;
  for (let c = 0; c < cycles; c++) {
    for (let i = 0; i < perCycle; i++) {
      const x = i / perCycle;
      let y = 0;
      if (x < 0.1) y = 0;
      else if (x < 0.15) y = abnormal ? -8 + Math.random() * 4 : -3;
      else if (x < 0.2) y = abnormal ? -4 + Math.random() * 3 : -1;
      else if (x < 0.25) y = 0;
      else if (x < 0.3) y = abnormal ? 28 + Math.random() * 12 : 18;
      else if (x < 0.35) y = abnormal ? -15 + Math.random() * 8 : -10;
      else if (x < 0.4) y = abnormal ? 8 + Math.random() * 5 : 5;
      else if (x < 0.45) y = 0;
      else if (x < 0.6) y = abnormal ? 5 + Math.sin(x * 40) * 3 : 3;
      else if (x < 0.7) y = 0;
      else y = abnormal ? Math.random() * 2 - 1 : 0;
      points.push(y);
    }
  }
  return points;
}

function generateVitalsForSeverity(severity: Severity): Vitals {
  const base = {
    stable: { hr: () => 65 + Math.random() * 20, o2: () => 96 + Math.random() * 3, bpS: () => 115 + Math.random() * 15, bpD: () => 70 + Math.random() * 10, temp: () => 36.5 + Math.random() * 0.5, rr: () => 14 + Math.random() * 4 },
    moderate: { hr: () => 90 + Math.random() * 20, o2: () => 92 + Math.random() * 5, bpS: () => 130 + Math.random() * 20, bpD: () => 80 + Math.random() * 15, temp: () => 37.5 + Math.random() * 0.8, rr: () => 18 + Math.random() * 5 },
    critical: { hr: () => 108 + Math.random() * 30, o2: () => 84 + Math.random() * 10, bpS: () => 150 + Math.random() * 30, bpD: () => 95 + Math.random() * 20, temp: () => 38.8 + Math.random() * 1.2, rr: () => 22 + Math.random() * 8 },
  }[severity];
  const hr = Math.round(base.hr());
  const o2 = Math.round(base.o2());
  const abnormal = o2 < 92 || hr > 110;
  return {
    heartRate: hr, oxygen: o2, bpSystolic: Math.round(base.bpS()),
    bpDiastolic: Math.round(base.bpD()), temperature: parseFloat(base.temp().toFixed(1)),
    respiratoryRate: Math.round(base.rr()), lastChecked: Date.now() - Math.floor(Math.random() * 90 * 60 * 1000),
    ecgPoints: generateEcgPoints(hr, abnormal),
  };
}

function generateVitalHistory(severity: Severity): VitalTrend[] {
  const now = Date.now();
  return Array.from({ length: 8 }, (_, i) => {
    const v = generateVitalsForSeverity(severity);
    return { timestamp: now - (7 - i) * 30 * 60 * 1000, heartRate: v.heartRate, oxygen: v.oxygen, bpSystolic: v.bpSystolic };
  });
}

