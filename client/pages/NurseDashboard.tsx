  const modPct = Math.round((patients.filter(p => p.severity === "moderate").length / patients.length) * 100);
  const stabPct = 100 - critPct - modPct;
  const taskCompletion = Math.round((patients.flatMap(p => p.tasks).filter(t => t.status === "completed").length / Math.max(patients.flatMap(p => p.tasks).length, 1)) * 100);
