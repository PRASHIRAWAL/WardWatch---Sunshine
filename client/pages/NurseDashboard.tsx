function generateTasksForSeverity(patientId: string, severity: Severity, abnormalVitals: boolean): NurseTask[] {
  const now = Date.now();
  const tasks: NurseTask[] = [];

  const makeTask = (label: string, due: number, priority: Priority, reasoning?: string): NurseTask => ({
    id: uid(), patientId, label, status: "pending", assignedAt: now, dueDuration: due,
    intelligence: getTaskIntelligence(label, priority), priority, reasoning,
  });

  if (severity === "critical") {
    tasks.push(makeTask("Check vitals immediately", 10 * 60 * 1000, "P1", "Critical patient requires immediate vital assessment"));
    tasks.push(makeTask("Notify attending doctor", 5 * 60 * 1000, "P1", "Medical oversight required for critical status"));
    tasks.push(makeTask("Administer IV medication", 15 * 60 * 1000, "P1", "Time-sensitive pharmacological intervention"));
    tasks.push(makeTask("Document critical event", 20 * 60 * 1000, "P2", "Compliance and handover documentation"));
    tasks.push(makeTask("Prepare emergency cart nearby", 30 * 60 * 1000, "P2", "Precautionary readiness for deterioration"));
  } else if (severity === "moderate") {
    tasks.push(makeTask("Monitor vitals every hour", 60 * 60 * 1000, "P2", "Trending required for moderate stability"));
    tasks.push(makeTask("Administer scheduled medication", 30 * 60 * 1000, "P2", "Per care plan protocol"));
    tasks.push(makeTask("Update patient chart", 90 * 60 * 1000, "P3", "Documentation for clinical continuity"));
    tasks.push(makeTask("Physiotherapy assessment", 2 * 60 * 60 * 1000, "P3", "Mobility and recovery assessment"));
  } else {
    tasks.push(makeTask("Routine vitals check", 4 * 60 * 60 * 1000, "P3", "Standard monitoring for stable patients"));
    tasks.push(makeTask("Patient comfort assessment", 6 * 60 * 60 * 1000, "P4", "Quality of care — pain, comfort, needs"));
    tasks.push(makeTask("Update discharge notes", 8 * 60 * 60 * 1000, "P4", "Preparation for potential discharge"));
  }

  if (abnormalVitals) {
    tasks.unshift(makeTask("⚠ Abnormal vitals — immediate review", 5 * 60 * 1000, "P1", "AI flagged: vitals outside safe range — auto-generated"));
    tasks[0].autoGenerated = true;
  }

  return tasks;
}

