// ECG Monitor
function ECGMonitor({ patient }: { patient: NursePatient }) {
  const canvasRef = useRef<HTMLCanvasElement>(null);
  const posRef = useRef(0);
  const pointsRef = useRef<number[]>(patient.vitals.ecgPoints || []);
  const abnormal = isVitalAbnormal(patient.vitals);
  const color = abnormal ? "#ef4444" : "#22c55e";

  useEffect(() => {
    const canvas = canvasRef.current;
    if (!canvas) return;
    const ctx = canvas.getContext("2d");
    if (!ctx) return;
    const W = canvas.width, H = canvas.height;
    const midY = H / 2;
    const pts = pointsRef.current;
    let frame = 0;
    let animId: number;
    const draw = () => {
      ctx.fillStyle = "rgba(0,0,0,0.15)";
      ctx.fillRect(0, 0, W, H);
      const visibleW = 4;
      ctx.clearRect(posRef.current * (W / pts.length) - visibleW, 0, visibleW + 2, H);
      const idx = posRef.current % pts.length;
      const x = (posRef.current % pts.length) * (W / pts.length);
      const prevIdx = (idx - 1 + pts.length) % pts.length;
      const prevX = prevIdx * (W / pts.length);
      const y = midY - pts[idx] * (midY / 30);
      const prevY = midY - pts[prevIdx] * (midY / 30);
      ctx.beginPath();
      ctx.moveTo(prevX, prevY);
      ctx.lineTo(x, y);
      ctx.strokeStyle = color;
      ctx.lineWidth = 1.5;
      ctx.shadowColor = color;
      ctx.shadowBlur = abnormal ? 6 : 3;
      ctx.stroke();
      posRef.current = (posRef.current + 1) % pts.length;
      frame++;
      animId = requestAnimationFrame(draw);
    };
    animId = requestAnimationFrame(draw);
    return () => cancelAnimationFrame(animId);
  }, [patient.id, color, abnormal]);

  return (
    <div className={`rounded-2xl overflow-hidden border ${abnormal ? "border-red-400/50 bg-black/80" : "border-green-400/30 bg-black/70"}`}>
      <div className="flex items-center justify-between px-3 py-1.5">
        <div className="flex items-center gap-1.5">
          <Activity className={`w-3 h-3 ${abnormal ? "text-red-400" : "text-green-400"}`} />
          <span className={`text-xs font-mono font-bold ${abnormal ? "text-red-400" : "text-green-400"}`}>
            ECG · {patient.vitals.heartRate} bpm
          </span>
        </div>
        {abnormal && (
          <span className="text-xs text-red-400 font-bold animate-pulse">ABNORMAL</span>
        )}
      </div>
      <canvas ref={canvasRef} width={340} height={56} className="w-full" style={{ imageRendering: "pixelated" }} />
    </div>
  );
}

